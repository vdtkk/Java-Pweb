package whileDongu;

public class Donguler {

	public static void main(String[] args) {
	
		
		//while döngüsü
		int i=1;
		while(i<10) {
			System.out.println(i);
				i++;			
		}
		System.out.println("dögü bitti");
			
		//do-while döngüsü
		
		int j=1;
		
		do {System.out.println(j);

		j++;}
			
		while(j<10);
		
		
		
	}

}
